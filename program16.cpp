#include<iostream>
using namespace std;

int main() {
	
	int num1;
	cout<<"enter a number:";
	cin>>num1;
	cout<<"\n Value before being incremented:"<<num1<<"\n";
	num1++;
	cout<<"value after being incrementd: "<<num1<<"\n";
	num1--;
	cout<<"Value before being decremented: "<<num1<<"\n";
	num1--;
	cout<<"value after being decrementd "<<num1;
	
}
